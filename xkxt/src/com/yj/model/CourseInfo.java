package com.yj.model;

import java.util.*;

public class CourseInfo {
	private String kh;
	private String km;
	private String gh;
    private String xm;
    private String sksj;
    private Integer xf;
    private Integer rs;
    private Integer dqrs;
    public CourseInfo(){
    }
	public String getKh() {
		return kh;
	}
	public void setKh(String kh) {
		this.kh = kh;
	}
	public String getKm() {
		return km;
	}
	public void setKm(String km) {
		this.km = km;
	}
	public String getXm() {
		return xm;
	}
	public void setXm(String xm) {
		this.xm = xm;
	}
	public String getSksj() {
		return sksj;
	}
	public void setSksj(String sksj) {
		this.sksj = sksj;
	}
	public Integer getXf() {
		return xf;
	}
	public void setXf(Integer xf) {
		this.xf = xf;
	}
	public Integer getRs() {
		return rs;
	}
	public void setRs(Integer rs) {
		this.rs = rs;
	}
	public Integer getDqrs() {
		return dqrs;
	}
	public void setDqrs(Integer dqrs) {
		this.dqrs = dqrs;
	}
	public String getGh() {
		return gh;
	}
	public void setGh(String gh) {
		this.gh = gh;
	}
}
